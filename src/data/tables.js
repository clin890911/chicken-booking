// 樓層摘要 helper
export function summarizeByFloor(tables) {
  const byFloor = { '1F': [], '2F': [] }
  tables.forEach(t => {
    if (byFloor[t.floor]) byFloor[t.floor].push(t)
  })
  return byFloor
}
